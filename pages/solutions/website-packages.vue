    (entries) => {
      entries.forEach((entry) => {
        if (entry.isIntersecting) {
          hostingVisible.value = true;
          observer.disconnect();
        }
      });
    },
    { threshold: 0.08 },
  );
  observer.observe(hostingRef.value);
  onUnmounted(() => observer.disconnect());
});
